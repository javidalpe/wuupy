    <p>Banks in some countries—the United States, Canada, and Australia—also require a <code>Routing number</code> to identify the specific bank account.</p>

    <p>Every country has a slightly different format for these account and routing numbers, and entering them incorrectly will cause transfers to fail.</p>

    <p>Because of this, we recommend creating validations in your bank account entry forms that enforce the required formats based on the country of the managed account’s bank. Below you’ll find a list of the expected format for routing and account numbers by country, including character counts where available. </p>
